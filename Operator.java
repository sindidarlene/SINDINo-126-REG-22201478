import javax.swing.JOptionPane;

public class Operator {
    public static void main(String args[]) {
        // Declare two integer variables 'a' and 'b' with values 10 and 5, respectively.
        int a = 10;
        int b = 5;

        // Use the addition operator (+) to add 'a' and 'b', then print the result.
        System.out.println(a + b);

        // Use the subtraction operator (-) to subtract 'b' from 'a', then print the result.
        System.out.println(a - b);

        // Use the multiplication operator (*) to multiply 'a' and 'b', then print the result.
        System.out.println(a * b);

        // Use the division operator (/) to divide 'a' by 'b', then print the result.
        System.out.println(a / b);

        // Use the modulus operator (%) to find the remainder of 'a' divided by 'b', then print the result.
        System.out.println(a % b);
    }
}

